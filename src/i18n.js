// Fallback: als een sleutel niet bestaat in de gekozen taal, val terug op NL.
// Fallback: if a key doesn't exist in the chosen language, fall back to NL.
export function getT(lang) {
  const dict = translations[lang] || translations.nl
  const fallback = translations.nl
  return function t(key) {
    return dict[key] ?? fallback[key] ?? key
  }
}

